package com.xiao.fish.domain.fishnet;

public class FishingNet {

	protected int netType;
	protected int width;
	protected int height;
	protected int fishNetBitmap;

	public int getNetType() {
		return netType;
	}

	public void setNetType(int netType) {
		this.netType = netType;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getFishNetBitmap() {
		return fishNetBitmap;
	}

	public void setFishNetBitmap(int fishNetBitmap) {
		this.fishNetBitmap = fishNetBitmap;
	}

}
