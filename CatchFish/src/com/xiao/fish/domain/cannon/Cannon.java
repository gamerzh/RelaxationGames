package com.xiao.fish.domain.cannon;

public class Cannon {
	protected int type;// 炮塔类型
	protected int goldRate;// 金币倍数
	protected int bitmapID;// 炮塔图片ID
	protected int headBimapID;// 炮塔炮口
	protected int width;
	protected int height;
	protected int head_width;
	protected int head_height;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getGoldRate() {
		return goldRate;
	}

	public void setGoldRate(int goldRate) {
		this.goldRate = goldRate;
	}

	public int getBitmapID() {
		return bitmapID;
	}

	public void setBitmapID(int bitmapID) {
		this.bitmapID = bitmapID;
	}

	public int getHeadBimapID() {
		return headBimapID;
	}

	public void setHeadBimapID(int headBimapID) {
		this.headBimapID = headBimapID;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getHead_width() {
		return head_width;
	}

	public void setHead_width(int head_width) {
		this.head_width = head_width;
	}

	public int getHead_height() {
		return head_height;
	}

	public void setHead_height(int head_height) {
		this.head_height = head_height;
	}
	
}
